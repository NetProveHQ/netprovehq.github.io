        /// <summary>
        /// Get a localized string for the given key in the current language.
        /// Falls back to English if key is missing.
        /// </summary>
        public string this[string key]
        {
            get
            {
                if (_strings[Current].TryGetValue(key, out var val)) return val;
                if (_strings[AppLanguage.English].TryGetValue(key, out var en)) return en;
                return key;
            }
        }

        /// <summary>
        /// Apply a language by injecting all string resources into the given
        /// ResourceDictionary as string resources with "S_" prefix.
        /// DynamicResource bindings pick up the changes instantly.
        /// </summary>
        public void Apply(AppLanguage lang, ResourceDictionary resources)
        {
            Current = lang;
            var dict = _strings.TryGetValue(lang, out var d) ? d : _strings[AppLanguage.English];
            foreach (var kv in dict)
                resources[$"S_{kv.Key}"] = kv.Value;

            // Ensure any keys only in English fallback are also present
            foreach (var kv in _strings[AppLanguage.English])
            {
                var rk = $"S_{kv.Key}";
                if (!resources.Contains(rk))
                    resources[rk] = kv.Value;
            }

            PropertyChanged?.Invoke(this, new PropertyChangedEventArgs("Item[]"));
        }

        /// <summary>Parse language name from settings string.</summary>
        public static AppLanguage Parse(string? name)
        {
            if (string.IsNullOrEmpty(name)) return AppLanguage.English;
            return Enum.TryParse<AppLanguage>(name, true, out var lang) ? lang : AppLanguage.English;
        }
